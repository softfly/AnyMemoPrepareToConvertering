package pl.softfly.amprepare;

public class AnyMemoConverterException extends Exception {

	private static final long serialVersionUID = 1L;

	public AnyMemoConverterException() {
		super();
	}

	public AnyMemoConverterException(String message) {
		super(message);
	}

	public AnyMemoConverterException(String message, Throwable cause) {
		super(message, cause);
	}

	public AnyMemoConverterException(Throwable cause) {
		super(cause);
	}

}
